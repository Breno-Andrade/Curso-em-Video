# Inserindo o valor digitado pelo usuário à uma variavel.
n = int(input('Digite um numero para ver sua tabuada: '))
# Separando o resultado da atribuição.
print('='*20)
# Indicando ao usuário o inicio da tabuada.
print('{} x  1 = {}\n{} x  2 = {}\n{} x  3 = {}\n{} x  4 = {}\n{} x  5 = {}'.format( n, n*1, n, n*2, n, n*3, n, n*4, n, n*5))
print('{} x  6 = {}\n{} x  7 = {}\n{} x  8 = {}\n{} x  9 = {}\n{} x 10 = {}'.format( n, n*6, n, n*7, n, n*8, n, n*9, n, n*10))
# Indicando ao usuário o fim da tabuada.
print('='*20)
