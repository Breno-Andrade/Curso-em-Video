print(' ====== Exercício 14 ====== ')
# Solicitando ao usuário a temperatura a ser convertida em fahrenheit.
c = float(input('Informe a temperatura em °C: '))
# Calculando a conversão de °C em °F.
f = c * 9 / 5 + 32
# Exibindo o Resultado ao usuário.
print('A temperatura de {}°C corresponde a {}°F '.format(c, f))
